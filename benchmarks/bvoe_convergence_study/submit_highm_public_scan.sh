#!/bin/bash
#SBATCH -J bvoe_highm
#SBATCH -p test
#SBATCH -N 1
#SBATCH --cpus-per-task=8
#SBATCH --mem=120G
#SBATCH -t 0-12:00:00
#SBATCH -o highm_public_scan_%j.out
#SBATCH -e highm_public_scan_%j.err

set -euo pipefail

ROOT=${ROOT:-${SLURM_SUBMIT_DIR:-$(cd -- "$(dirname "${BASH_SOURCE[0]}")" && pwd)}}
PYSCF_PYTHON=${PYSCF_PYTHON:-python}

# The benchmark code caches the FCI reference orbitals/CI vectors and uses
# that fixed gauge for every M point, so threaded DMRG runs remain comparable
# against the same reference.
export OMP_NUM_THREADS=${SLURM_CPUS_PER_TASK:-8}
export MKL_NUM_THREADS=${SLURM_CPUS_PER_TASK:-8}
export OPENBLAS_NUM_THREADS=${SLURM_CPUS_PER_TASK:-8}
export BVOE_THREADS=${SLURM_CPUS_PER_TASK:-8}
export BVOE_MP_CONTEXT=${BVOE_MP_CONTEXT:-spawn}
export BVOE_SWEEPS=${BVOE_SWEEPS:-100}
export BVOE_ROOT_BUFFER=${BVOE_ROOT_BUFFER:-8}
export BVOE_FORCE_RECOMPUTE=${BVOE_FORCE_RECOMPUTE:-1}
export BVOE_ROOT_LOCK_OVERLAP=${BVOE_ROOT_LOCK_OVERLAP:-0.90}
export BVOE_ROOT_LOCK_MARGIN=${BVOE_ROOT_LOCK_MARGIN:-0.10}
export BVOE_DAV_THRD=${BVOE_DAV_THRD:-1e-14}
export BVOE_DAV_MAX_ITER=${BVOE_DAV_MAX_ITER:-8000}
export BVOE_DAV_DEF_MAX_SIZE=${BVOE_DAV_DEF_MAX_SIZE:-80}
export BVOE_SWEEP_TOL=${BVOE_SWEEP_TOL:-1e-14}
export BVOE_MPS_COEFF_CUTOFF=${BVOE_MPS_COEFF_CUTOFF:-1e-16}
export BVOE_CASSCF_CONV_TOL=${BVOE_CASSCF_CONV_TOL:-1e-12}
export BVOE_CASSCF_CONV_TOL_GRAD=${BVOE_CASSCF_CONV_TOL_GRAD:-1e-9}
export BVOE_CASSCF_MAX_CYCLE=${BVOE_CASSCF_MAX_CYCLE:-300}
export BVOE_FCI_POLISH_CONV_TOL=${BVOE_FCI_POLISH_CONV_TOL:-1e-14}
export BVOE_FCI_POLISH_MAX_CYCLE=${BVOE_FCI_POLISH_MAX_CYCLE:-300}
export BVOE_FCI_POLISH_MAX_SPACE=${BVOE_FCI_POLISH_MAX_SPACE:-80}
export BVOE_FCI_POLISH_PSPACE_SIZE=${BVOE_FCI_POLISH_PSPACE_SIZE:-2000}
export BVOE_FCI_POLISH_SCAN_ROOTS=${BVOE_FCI_POLISH_SCAN_ROOTS:-20}
export BVOE_FCI_POLISH_SPIN_TOL=${BVOE_FCI_POLISH_SPIN_TOL:-1e-6}
export BVOE_FCI_DEGENERACY_TOL=${BVOE_FCI_DEGENERACY_TOL:-1e-4}
export BVOE_MIN_DERIVATIVE_ROOT_OVERLAP=${BVOE_MIN_DERIVATIVE_ROOT_OVERLAP:-0.50}
export BVOE_POINT_TIMEOUT_S=${BVOE_POINT_TIMEOUT_S:-1800}

cd "$ROOT"

echo "High-M public BVOE scan start: $(date)"
echo "Python: $PYSCF_PYTHON"
echo "BVOE_SWEEPS=$BVOE_SWEEPS BVOE_THREADS=$BVOE_THREADS BVOE_ROOT_BUFFER=$BVOE_ROOT_BUFFER"
echo "BVOE_MP_CONTEXT=$BVOE_MP_CONTEXT"
echo "BVOE_FORCE_RECOMPUTE=$BVOE_FORCE_RECOMPUTE"
echo "BVOE_ROOT_LOCK_OVERLAP=$BVOE_ROOT_LOCK_OVERLAP BVOE_ROOT_LOCK_MARGIN=$BVOE_ROOT_LOCK_MARGIN"
echo "BVOE_DAV_THRD=$BVOE_DAV_THRD BVOE_SWEEP_TOL=$BVOE_SWEEP_TOL"
echo "BVOE_FCI_POLISH_CONV_TOL=$BVOE_FCI_POLISH_CONV_TOL BVOE_FCI_POLISH_SCAN_ROOTS=$BVOE_FCI_POLISH_SCAN_ROOTS"
echo "BVOE_FCI_DEGENERACY_TOL=$BVOE_FCI_DEGENERACY_TOL"
echo "BVOE_MIN_DERIVATIVE_ROOT_OVERLAP=$BVOE_MIN_DERIVATIVE_ROOT_OVERLAP BVOE_POINT_TIMEOUT_S=$BVOE_POINT_TIMEOUT_S"
echo "BVOE_SYSTEMS=${BVOE_SYSTEMS:-all}"

"$PYSCF_PYTHON" -m py_compile run_bvoe_phase2.py plot_bvoe_phase2.py audit_bvoe_references.py test_root_matching_protocol.py
"$PYSCF_PYTHON" test_root_matching_protocol.py
if [[ -n "${BVOE_SYSTEMS:-}" ]]; then
  "$PYSCF_PYTHON" run_bvoe_phase2.py $BVOE_SYSTEMS
else
  "$PYSCF_PYTHON" run_bvoe_phase2.py
fi
"$PYSCF_PYTHON" plot_bvoe_phase2.py
"$PYSCF_PYTHON" audit_bvoe_references.py

echo "High-M public BVOE scan done: $(date)"
